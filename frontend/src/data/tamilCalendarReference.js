export const getTamilMonth = (index) => TAMIL_MONTHS.find(m => m.index === index);
export const getNakshatra = (index) => TAMIL_NAKSHATRAS.find(n => n.index === index);
export const getYoga = (index) => TAMIL_YOGAS.find(y => y.index === index);
export const getKarana = (index) => TAMIL_KARANAS.find(k => k.index === index);
export const getThithi = (index) => TAMIL_THITHIS.find(t => t.index === index);
export const getRasi = (index) => TAMIL_RASIS.find(r => r.index === index);
export const getAnandadiYoga = (index) => TAMIL_ANANDADI_YOGAS.find(a => a.index === index);
export const getSamvatsara = (index) => TAMIL_SAMVATSARAS.find(s => s.index === index);

export const getSamvatsaraForYear = (englishYear) => {
  // Tamil New Year starts in mid-April
  // Reference: 2025 is விசுவாவசு (index 39)
  const baseYear = 2025;
  const baseIndex = 39;
  const diff = englishYear - baseYear;
  const index = ((baseIndex - 1 + diff) % 60) + 1;
  return getSamvatsara(index);
};

